    &nbsp;&nbsp;&nbsp;&nbsp; O Ministério da educação – MEC possui um PROGRAMA DE EXTENSÃO UNIVERSITÁRIO denominado PROEXT, que é um instrumento que abrange programas e projetos de extensão universitária, 
    com ênfase na inclusão social nas suas mais diversas dimensões, visando aprofundar ações políticas que venham fortalecer a institucionalização da extensão no âmbito das Instituições Federais, 
    Estaduais e Municipais de Ensino Superior tendo como objetivos de apoiar as instituições no desenvolvimento das ações de extensão, potencializar e ampliar estas ações, aproximar os estudantes 
    com realidades concretas e da troca de saberes acadêmicos e populares e dotar as Instituições de ensino Superiores de condições de gestão de suas atividades acadêmicas de extensão.
    Assim para participar deste programa dos governos, emergiu um movimento de diferentes áreas que compõem um programa chamado “DESENVOLVIMENTO E APLICAÇÃO DE TECNOLOGIA SOCIAL E ASSISTIVA 
    COM O USO DA INFORMÁTICA E DO GEOPROCESSAMENTO”. Este foi composto por três projetos com o objetivo geral de utilizar a informática e internet como ferramentas mediadoras de mudanças da 
    qualidade de vida para os sujeitos participantes do programa, através de suas atividades de inclusão e da responsabilidade social que cabe aos Institutos Federais Educação, Ciência e Tecnologia.
    Foi submetido ao PREXT 2012 em maio de 2011 e ficou classificado, sendo contemplado com fomento de R$150.000,00. No planejamento do programa, maior parte deste recurso era destinado ao projeto Geosaude,
    por suas especificidades e necessidades de computadores, softwares e outros recursos materias além do numero necessário de bolsistas para executa-lo.
    O Programa Geosaude é a integração dos cursos técnicos em Geoprocessamento e Enfermagem, e do Tecnólogo em Análise e Desenvolvimento de Sistemas (TADS), ambos ofertados pelo Instituto Federal de Educação, 
    Ciência e Tecnologia do Rio Grande do Sul (Campus Rio Grande). Sua característica é de extensão, e tem como objetivos específicos:<p>
<ul>
    <li>Gerar um Sistema de Informações Geográficas com dados relativos aos casos de Hepatite A e Dengue do município do Rio Grande;</li>
    <li>Habilitar alunos do curso de Geoprocessamento do IFRS – Campus Rio Grande na elaboração de um SIG;</li>
    <li>Disponibilizar as informações geradas e organizadas na forma de um servidor de mapas estáticos e dinâmicos;</li>
    <li>Capacitar técnicos da Prefeitura Municipal para a utilização da ferramenta SIG no dia a dia da gestão pública;</li>
    <li>Aproximar os alunos do Curso técnico de Enfermagem das tecnologias digitais aplicadas a profissão e planejar ações de educação em saúde e prevenção dos agravos de doenças previsíveis voltadas as necessidade locais.</li>
</ul>
    &nbsp;&nbsp;&nbsp;&nbsp; O Geosaude além de sua característica interdisciplinar e multiprofissional também possui parcerias externas com a Secretaria Municipal de Coordenação e Planejamento, Secretaria Municipal da Saúde e Instituto Brasileiro de Geografia e Estatística. Esta aproximação possibilita o desenvolvimento do projeto, visto que os dados trabalhados são gerados pelos registros destes departamentos e os resultados do trabalho realizado no projeto são disponibilizados a estes, contribuindo com seu trabalho. Esta parceria tem gerado demandas impulsionando a busca de mais fomentos para viabilizar atender as necessidades surgidas. O IFRS passou a contribuir com mais recursos aumentando o numero de bolsistas.
    A equipe de trabalho é dividida em quase 30 bolsistas, que possuem suas tarefas divididas por cursos, da seguinte forma: os alunos do curso TADS tem como responsabilidade o suporte à geração e edição do banco de dados, a inserção dos dados cartográficos na web para que as secretarias municipais tenham acesso, a atualização da homepage do Projeto entre outras da área.
    Os alunos do curso Técnico em Geoprocessamento tem a responsabilidade de gerar o banco de dados das ocorrências, os mapas temáticos, pontuando os casos de Dengue e Hepatite A baseados nos dados fornecidos pela Secretaria municipal de saúde, de atualizar o mapa base municipal, participam das capacitações periódicas ofertadas pelo projeto a equipe dos gestores públicos municipais, a fim de que se tornem proficientes na geração de produtos cartográficos, atualização dos dados no SIG e manuseio de softwares e aparelhos receptores de sinal GPS.
    Os alunos do curso Técnico em Enfermagem realizam ações de educação em saúde nas comunidades baseados nos resultados obtidos pelos mapeamentos dos agravos em questão e participam de ações desenvolvidas pela secretaria de saúde na comunidade, instruindo os moradores sobre a prevenção da Dengue e da Hepatite A. O projeto conseguiu atender todas as suas metas antes do período planejado, e as secretarias municipais integrantes continuam gerando novas demandas. Os bolsistas estão atendendo as solicitações sem grandes dificuldades. 
    A ampliação e a diversificação das ações desenvolvidas pelo aumento das demandas modificaram as características do projeto, o aderindo um status de programa. Assim surgiu a necessidade de modificar o projeto para edição do ano de 2013, passando de projeto para programa. Com estes fatos tem-se a perspectiva de que o Programa e as parcerias tornem-se contínuo e permanente.
